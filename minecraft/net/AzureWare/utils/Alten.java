package net.AzureWare.utils;

public class Alten {
	public String email;
	public String username;
	
	public Alten(String email, String username) {
		this.email = email;
		this.username = username;
	}
	
	public String getEmail() {
		return this.email;
	}

	public String getUsername() {
		return this.username;
	}
}
